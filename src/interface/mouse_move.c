#include "rt.h"

int		mouse_move(int x, int y, t_stuff *e)
{
	if (x > 0 && x < WIN_X - WIDTH && y > (WIN_Y - LENGTH) * 2 && y < (WIN_Y - LENGTH) * 3)
		mouse_move_new_obj(x, y, e);
}
